def _split_blocks(message, block_size=16, require_padding=True):
    # print(message)
    assert len(message) % block_size == 0 or not require_padding
    return [message[i:i + 16] for i in range(0, len(message), block_size)]


def get_first_round_value(plaintext: bytes, key: bytes):
    if (type(plaintext) == bytes) & (type(key) == bytes):
        plaintext = pad(plaintext)
        blocks = []
        for plaintext_block in _split_blocks(plaintext):
            block = np.array(_get_first_value(plaintext_block, key))
            for i in range(block.shape[0]):
                for j in range(block.shape[1]):
                    blocks.append(get_HW(block[i][j]))
        return blocks
    else:
        return "please check the type of input"


